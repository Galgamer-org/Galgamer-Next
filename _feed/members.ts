export function getMember(name: string): Member {
    let result: Member | undefined;
    if (name === undefined) {
        result = undefined;
    } else {
        result = staffs[name] ?? members[name];
    }
    if (result === undefined) {
        //console.error(`Member ${name} not found`);
        result = {
            name: name,
            photo: "/member-icon/default.jpeg",
        };
    }
    return result;
}